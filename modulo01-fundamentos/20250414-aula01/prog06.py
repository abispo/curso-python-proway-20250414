# Operadores em Python

# Operadores de atribuição

if __name__ == "__main__":

    contador = 1
    contador += 3               # contador = contador + 3
    print(contador)             # 4

    contador *= 3               # contador = contador * 3
    print(contador)             # 12

    contador = contador - 5     # contador -= 5
    print(contador)             # 7